package com.xworkz.september.things;

public class Operators {
	public static void main(String args[]) {
		int a = 45;
		int b = 15;
		System.out.println((a + b));
		System.out.println((a - b));
		System.out.println((a * b));
		System.out.println((a / b));
		System.out.println(a % b);
		System.out.println(a = b);
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a < b);
		System.out.println(a > b);
		System.out.println(a <= b);
		System.out.println(a >= b);
		System.out.println((a > b) ? true : false);
		System.out.println((a++));
		System.out.println((++a));
		System.out.println((--b));
		System.out.println((b--));
		System.out.println((a += b));
		System.out.println((a -= b));
		System.out.println((a *= b));
		System.out.println((a %= b));
		System.out.println((a /= b));

	}

}
